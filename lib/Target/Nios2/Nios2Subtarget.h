#ifndef NIOS2SUBTARGET_H
#define NIOS2SUBTARGET_H

#include "llvm/Target/TargetSubtarget.h"
#include "llvm/Target/TargetMachine.h"

#include <string>

namespace llvm {

class Nios2Subtarget : public TargetSubtarget {

public:

protected:

  // IsLittle - The target is Little Endian
  bool IsLittle;
  bool HasMul;          ///< Has an integer multiply instruction.
  bool HasMulx;         ///< Has an integer extended multiply instruction.
  bool HasDiv;          ///< Has an integer divide instruction.

  // isLinux - Target system is Linux.
  bool IsLinux;

  InstrItineraryData InstrItins;

public:

  /// This constructor initializes the data members to match that
  /// of the specified triple.
  Nios2Subtarget(const std::string &TT, const std::string &FS, bool little);
  
  /// ParseSubtargetFeatures - Parses features string setting specified 
  /// subtarget options.  Definition of function is auto generated by tblgen.
  std::string ParseSubtargetFeatures(const std::string &FS,
                                     const std::string &CPU);

  bool isLittle() const { return IsLittle; }

  /// Features related to the presence of specific instructions.
  bool hasMul()  const { return HasMul; }
  bool hasMulx()  const { return HasMulx; }
  bool hasDiv()  const { return HasDiv; }

  bool isLinux() const { return IsLinux; }
};
} // End llvm namespace

#endif
